  /**
   * Detects if the string contains any non-ASCII characters.
   *
   * @param text - The text to inspect.
   * @returns True if non-ASCII character is found.
   */
  public static hasNonAscii(text: string): boolean {
    return /[^\u0000-\u007F]/.test(text);
  }

  /**
   * Transliterates non-ASCII characters to their phonetic ASCII equivalents.
   *
   * @param text - The text to transliterate.
   * @returns Phonetically romanized ASCII string.
   */
  public static transliterate(text: string): string {
    let result = '';
    for (const char of text) {
      const mapped = Transliterator.MAP[char];
      if (mapped !== undefined) {
        result += mapped;
      } else {
        result += char;
      }
    }
    return result;
  }
